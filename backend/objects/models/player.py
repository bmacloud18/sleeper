class Player:
    def __init__(self, id, first_name, last_name, team, number, position, avg_round, avg_pick, slots, slot_max):
        self.id = id
        self.first_name = first_name
        self.last_name = last_name
        self.team = team
        self.number = number
        self.position = position
        self.avg_round = avg_round
        self.avg_pick = avg_pick
        self.slots = slots
        self.slot_max = slot_max